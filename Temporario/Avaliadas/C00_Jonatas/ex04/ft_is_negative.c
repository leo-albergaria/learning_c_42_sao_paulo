#include <unistd.h>

void	ft_is_negative(int n)
{
	if (n < 0)
		write(1, 65, 1);
	else
		write(1, 66, 1);
}

int	main(void)
{
	ft_is_negative(-1);
	write(1, "\n", 1);
}
