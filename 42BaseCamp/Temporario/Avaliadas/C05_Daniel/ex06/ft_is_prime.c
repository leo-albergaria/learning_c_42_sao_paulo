int	ft_is_prime(int nb)
{
	int	i;

	if (nb < 2)
		return (0);
	if (nb > 2)
	{
		i = 2;
		while (i < 46341)
		{
			if ((i * i) / 2 < nb)
				if (nb % i == 0)
					return (0);
			i++;
		}
	}
	return (1);
}
